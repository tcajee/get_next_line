#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	a;

	i = 0;
	a = '\n';
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &a, 1);
}
