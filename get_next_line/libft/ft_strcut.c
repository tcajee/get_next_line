#include "libft.h"

char	*ft_strcut(char *s, char c)
{
	int		i;
	int		p;

	p = 0;
	i = 0;
	if (!s[i])
		return (NULL);
	while (s[i] != c && s[i])
		i++;
	i++;
	while (s[i])
	{
		s[p] = s[i];
		i++;
		p++;
	}
	while (s[p])
	{
		s[p] = '\0';
		p++;
	}
	return (s);
}
