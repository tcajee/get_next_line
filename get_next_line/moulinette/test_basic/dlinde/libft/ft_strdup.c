#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		n;

	n = 0;
	while (s1[n] != '\0')
		n++;
	if (!(s2 = (char *)malloc(sizeof(char) * (n + 1))))
		return (NULL);
	n = 0;
	while (s1[n] != '\0')
	{
		s2[n] = s1[n];
		n++;
	}
	s2[n] = '\0';
	return (s2);
}
