#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	a;

	a = 0;
	i = 0;
	while (s1[i] != '\0')
	{
		i++;
	}
	while (s2[a] != '\0' && a < n)
	{
		s1[i] = s2[a];
		i++;
		a++;
	}
	s1[i] = '\0';
	return (s1);
}
