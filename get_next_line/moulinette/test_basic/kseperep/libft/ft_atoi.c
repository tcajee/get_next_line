#include "libft.h"

int				ft_atoi(const char *str)
{
	int			i;
	int			n;

	n = 1;
	while ((*str >= '\t' && *str <= '\r') || *str == 32 || *str == '0')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			n = -1;
		str++;
	}
	i = 0;
	while (*str >= '0' && *str <= '9' && *str)
		i = (i * 10) + (*str++ - '0');
	return (i * n);
}
