#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		j;
	int		index;

	j = ft_strlen(s1);
	index = 0;
	dest = (char*)malloc(sizeof(char) * j + 1);
	if (dest == NULL)
		return (NULL);
	while (s1[index] != '\0')
	{
		dest[index] = s1[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}
