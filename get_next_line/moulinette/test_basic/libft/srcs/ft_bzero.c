#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	cnt;
	char	*chars;

	cnt = 0;
	chars = s;
	if (s)
		while (cnt < n)
		{
			chars[cnt] = 0;
			cnt++;
		}
}
