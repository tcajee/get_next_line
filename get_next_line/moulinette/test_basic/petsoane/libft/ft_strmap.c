#include <stdlib.h>
#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char c))
{
	char	*str;
	int		size;
	int		i;

	size = ft_strlen(s);
	i = 0;
	str = (char *)malloc(size + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
