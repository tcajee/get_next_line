#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		t;
	size_t	p;

	t = 0;
	while (s1[t])
		t++;
	p = 0;
	while (s2[p] != '\0' && p < n)
	{
		s1[t + p] = s2[p];
		p++;
	}
	s1[t + p] = '\0';
	return (s1);
}
