#include "libft.h"

void			ft_putnbr(int n)
{
	unsigned int a;

	a = (unsigned int)n;
	if (n < 0)
	{
		ft_putchar('-');
		a *= -1;
	}
	if (a > 9)
		ft_putnbr(a / 10);
	ft_putchar((char)(a % 10 + '0'));
}
